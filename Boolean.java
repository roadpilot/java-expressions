public class Boolean {
    // main method
    public static void main(String[] args){
        int attendees = 4;
        boolean largeVenue;
        //if statement example
        if (attendees >= 5){
            largeVenue = true;
        }
        else {
            largeVenue = false;
        }
        System.out.println(largeVenue);
    }   
}